import request from '@/utils/request';


/**
 * 查询邮箱设置
 */
export function queryEmailSet() {
  return request({
    url: 'emailset',
    method: 'get',
  })
}

/**
 * 修改邮箱设置
 *
 * @param emailSet 邮箱设置信息
 */
export function updateEmailSet(emailSet) {
  return request({
    url: 'emailset',
    method: 'put',
    data: emailSet
  })
}
